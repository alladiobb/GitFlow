#Caso você comece Adicionar/Editar um outro arquivo e não fez o PULL antes de começar a trabalhar
#Mas você não quer perde o que estava trabalhando use o comando:
#STASH Significa Esconder em português
git stash

#Para listar as alterações
git stash list

#Pega a última alteração escondida e traz para usa-la 
git stash pop